/* Should return data of n'th node from the end of linked list.
*  head: head of the linked list
*  n: nth node from end to find
*/
int count(Node *head){
    Node *temp = head;
    int c =0;
    while(temp){
        c++;
        temp= temp->next;
    }
    return c;
}
int getNthFromLast(Node *head, int n)
{
       int c = count(head);
       int diff = c -n;
       
       Node *temp = head;
       
       if(diff<0)
            return -1;
       
       while(temp){
         if(diff ==0){
             return temp->data;
         }
         diff--;
         temp = temp->next;
       }
       return -1;
}

