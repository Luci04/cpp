struct node *reverse (struct node *head, int k)
{ 
    node* prevPtr = NULL;
    node* currPtr = head;
    node* nextPtr;
    int count = 0;
    while(currPtr != NULL and count < k){
        nextPtr = currPtr->next;
        currPtr->next = prevPtr;
        prevPtr = currPtr;
        currPtr = nextPtr;
        count++;
    }
    if(nextPtr != NULL){
      head->next =  reverse(nextPtr, k);
    }
    return prevPtr;
}